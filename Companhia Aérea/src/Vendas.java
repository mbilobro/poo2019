public class Vendas {
    public int passageiro;
    public int aviao;
    public double precoPassagem;

    public int getPassageiro() {
        return passageiro;
    }

    public void setPassageiro(int passageiro) {
        this.passageiro = passageiro;
    }

    public int getAviao() {
        return aviao;
    }

    public void setAviao(int aviao) {
        this.aviao = aviao;
    }

    public double getPrecoPassagem() {
        return precoPassagem;
    }

    public void setPrecoPassagem(double precoPassagem) {
        this.precoPassagem = precoPassagem;
    }
    
}
